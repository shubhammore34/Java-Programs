public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;
        
        System.out.println(a + b); //14
        System.out.println(a - b); //6
        System.out.println(a * b); //40
        System.out.println(a / b); //2
        System.out.println(a % b); //2
        System.out.println(a % 2.2); //1.2
    }
}